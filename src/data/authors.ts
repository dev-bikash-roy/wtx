export async function getAuthorByHandle(handle: string) {
  const authors = await getAuthors()
  let author = authors.find((author) => author.handle === handle)
  if (!author?.id) {
    // for demo purposes, if no author found, return the first one
    author = authors[0]
  }

  return author
}

